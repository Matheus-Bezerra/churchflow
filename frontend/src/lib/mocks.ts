export function getBirthdaysThisMonth(): User[] {
  const now = new Date()
  const month = now.getMonth() + 1
  return mockUsers
    .filter((u) => !u.deleted_at && u.birth_date)
    .filter((u) => {
      const bMonth = parseInt(u.birth_date!.split('-')[1], 10)
      return bMonth === month
    })
}
